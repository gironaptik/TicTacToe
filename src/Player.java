import java.util.*;



public class Player {
	
	private String name;
	private Map<Integer, String> userTurn;
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Map<Integer, String> getUserTurn() {
		return userTurn;
	}
	public void setUserTurn(Map<Integer, String> userTurn) {
		this.userTurn = userTurn;
	}
	


	
	
}
